# Strategy 1: Risk Management During Fear Days

# Recommendation:

# During Fear market sentiment, traders should reduce their position size (Size USD) and avoid excessive trading. Fear periods often show lower profitability and higher downside risk, so capital preservation should be the priority.

# Reason:

# Based on:
# Average PnL analysis
# Drawdown proxy
# Position size comparison



# Strategy 2: Increase Trading Activity During Greed Days

# Recommendation:

# During Greed market sentiment, frequent traders can increase trading activity if the analysis shows higher average PnL and win rate. However, traders should still use predefined risk management rules.

# Reason:

# Based on:
# Trade frequency
# Win rate
